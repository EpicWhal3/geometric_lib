def area(a, h):
    '''Возвращает площадь треугольника
            
            Параметры:
                a (int): длина основания треугольника
                h (int): длина высоты треугольника
            
            Возвращаемое значение - (int) число, результат вычислений''' 
    return (a * h) / 2 

def perimeter(a, b, c):
    '''Получает числа a, b, c - стороны треугольника. Возвращает результат их суммы''' 
    return a + b + c